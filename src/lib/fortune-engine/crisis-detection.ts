/**
 * GPT3(チャットAIプロンプト・口調設計)の「安全ガードレール(多層防御)」Layer 2 実装。
 * 自傷・自殺・DV・虐待等の示唆をLLM呼び出し前に検知し、最優先でcrisisフローへ回す。
 * キーワード方式は簡易実装であり、Phase2で分類モデル等への強化を検討すること
 * (誤検知・見逃し両方のリスクがあるため、あくまで一次フィルタとして扱う)。
 */

const CRISIS_KEYWORDS = [
  "死にたい",
  "消えたい",
  "自殺",
  "リストカット",
  "殴られ",
  "暴力を受け",
  "虐待",
  "殺したい",
  "死ぬしかない",
];

export function detectCrisis(text: string): boolean {
  return CRISIS_KEYWORDS.some((kw) => text.includes(kw));
}

/**
 * GPT3のFew-shot例(crisis)に準拠した固定応答。
 * この文面はLLMに生成させず、常に同じ内容を確実に返す(安全性を最優先するため)。
 */
export function crisisResponseMessage(): string {
  return [
    "今、それほど辛い気持ちを抱えているんですね。話してくれて、ありがとう。あなたは一人じゃありません。",
    "わたしはいつでもここにいますが、今の気持ちは、専門の方に聞いてもらうことも大切です。",
    "「いのちの電話」0120-783-556 では、あなたの話を受け止めてくれる人がいます。",
    "どうか、一人で抱え込まないでくださいね。",
  ].join("\n");
}
